// 대입 연산자 이해하기 (=)
// 변수에 값을 할당하기 위함
// = 연산자를 사용

#include <iostream>

using namespace std;

int main()
{
    int x = 1;
    int y = 9;

    int z = x + y;

    double i = 1.2;
    double j = 2.3;
    
    double k = i + j;

    cout << "x + y = " << z << endl;
    cout << "i + j = " << k << endl;

    return 0;
}
