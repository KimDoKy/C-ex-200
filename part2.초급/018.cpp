// 정수형 변수 이해하기 (int)
// -1, 0, 1 등을 정수라 하고, int 키워드로 선언

#include <iostream>

using namespace std;

int main()
{
    int positive = 100;
    int negative = -200;
    int ascii_value = 'A';

    cout << "양수 값: " << positive << endl;
    cout << "음수 값: " << negative << endl;
    cout << "아스키 값: " << ascii_value << endl;

    return 0;
}
