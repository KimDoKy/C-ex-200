// 상수 배우기(const)
// 변하지 않고 변할 수 없는 값

#include <iostream>

using namespace std;

int main()
{
    const int CREATE_VICTORY_SALSU = 612;
    const int CREATE_VICTORY_GWIJU = 1019;

    cout << "고구려 살수대첩 연도: " << CREATE_VICTORY_SALSU << "년" << endl;
    cout << "고려 귀주대첩 연도: " << CREATE_VICTORY_GWIJU << "년" << endl;
    return 0;
}
