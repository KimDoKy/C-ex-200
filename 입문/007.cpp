// 자료형 이해하기
// (char, int, double, bool, string)
// C++에서 사용하는 자료형

#include <iostream>
#include <string>

using namespace std;

int main()
{
    char character = 'C';
    int integer = 100;
    double percision = 3.14159;
    bool is_true = true;
    string word = "Hello World";

    cout << "char : " << character << endl;
    cout << "int : " << integer << endl;
    cout << "double : " << percision << endl;
    cout << "bool : " << is_true << endl;
    cout << "string : " << word << endl;

    return 0;
}

